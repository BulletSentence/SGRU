package br.edu.ifma.ticketif.core;

public class LogErro {
    
    private String data;
    private String execao;
    private String causa;

    /**
     * @return the data
     */
    public String getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(String data) {
        this.data = data;
    }

    /**
     * @return the execao
     */
    public String getExecao() {
        return execao;
    }

    /**
     * @param execao the execao to set
     */
    public void setExecao(String execao) {
        this.execao = execao;
    }

    /**
     * @return the causa
     */
    public String getCausa() {
        return causa;
    }

    /**
     * @param causa the causa to set
     */
    public void setCausa(String causa) {
        this.causa = causa;
    }
    
}
